CREATE SEQUENCE person_id_seq AS BIGINT START WITH 1 INCREMENT BY 1;

CREATE TABLE person(
	id BIGINT GENERATED BY DEFAULT AS SEQUENCE person_id_seq PRIMARY KEY,
	name VARCHAR(100) NOT NULL,
	cpf VARCHAR(14) NOT NULL,
	email VARCHAR(100)
);

CREATE SEQUENCE phone_id_seq AS BIGINT START WITH 1 INCREMENT BY 1;
CREATE TABLE phone (
  id BIGINT GENERATED BY DEFAULT AS SEQUENCE phone_id_seq PRIMARY KEY,
  ddd INT NOT NULL,
  number VARCHAR(10) NOT NULL,
  id_person BIGINT NOT NULL,
  FOREIGN KEY (id_person) REFERENCES person(id)
);


CREATE SEQUENCE address_id_seq AS BIGINT START WITH 1 INCREMENT BY 1;
CREATE TABLE address (
  id BIGINT GENERATED BY DEFAULT AS SEQUENCE address_id_seq PRIMARY KEY,
  state VARCHAR(100),
  city VARCHAR(100),
  neighborhood VARCHAR(100),
  number VARCHAR(5),
  id_person BIGINT NOT NULL,
  FOREIGN KEY (id_person) REFERENCES person(id)
);